package com.nt.vo;

public class CricketerVO {
	private String cno;
	private String cname;
	private String cadd;
	private String totalruns;
	private String totalmatches;
	private String numofnotouts;
	private String runsconceded;
	private String wicketstaken;
	public String getCno() {
		return cno;
	}
	public void setCno(String cno) {
		this.cno = cno;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCadd() {
		return cadd;
	}
	public void setCadd(String cadd) {
		this.cadd = cadd;
	}
	public String getTotalruns() {
		return totalruns;
	}
	public void setTotalruns(String totalruns) {
		this.totalruns = totalruns;
	}
	public String getTotalmatches() {
		return totalmatches;
	}
	
	public String getNumofnotouts() {
		return numofnotouts;
	}
	public void setNumofnotouts(String numofnotouts) {
		this.numofnotouts = numofnotouts;
	}
	public void setTotalmatches(String totalmatches) {
		this.totalmatches = totalmatches;
	}
	public String getRunsconceded() {
		return runsconceded;
	}
	public void setRunsconceded(String runsconceded) {
		this.runsconceded = runsconceded;
	}
	public String getWicketstaken() {
		return wicketstaken;
	}
	public void setWicketstaken(String wicketstaken) {
		this.wicketstaken = wicketstaken;
	}
	

}
